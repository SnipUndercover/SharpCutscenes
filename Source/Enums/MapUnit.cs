namespace Celeste.Mod.SharpCutscenes.Enums;

/// <summary>
///   The units used in mapping.
/// </summary>
public enum MapUnit
{
    /// <summary>
    ///   A single pixel.
    /// </summary>
    Pixel = 1,

    /// <summary>
    ///   A single tile, which is 8 pixels.
    /// </summary>
    Tile = 8,

    /// <summary>
    ///   The screen width, which is 320 pixels.
    /// </summary>
    ScreenWidth = 320
}
